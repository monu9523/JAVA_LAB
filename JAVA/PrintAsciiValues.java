public class PrintAsciiValues {
     public static void main(String[] args) {
        System.out.println("ASCII values of numbers (0-9):");
        System.out.println("Character: 0, ASCII Value: " + (int)'0');
        System.out.println("Character: 1, ASCII Value: " + (int)'1');
        System.out.println("Character: 2, ASCII Value: " + (int)'2');
        System.out.println("Character: 3, ASCII Value: " + (int)'3');
        System.out.println("Character: 4, ASCII Value: " + (int)'4');
        System.out.println("Character: 5, ASCII Value: " + (int)'5');
        System.out.println("Character: 6, ASCII Value: " + (int)'6');
        System.out.println("Character: 7, ASCII Value: " + (int)'7');
        System.out.println("Character: 8, ASCII Value: " + (int)'8');
        System.out.println("Character: 9, ASCII Value: " + (int)'9');
        System.out.println("\nASCII values of uppercase letters (A-Z):");
        System.out.println("Character: A, ASCII Value: " + (int)'A');
        System.out.println("Character: B, ASCII Value: " + (int)'B'); 
        // ... (continue for all uppercase letters)
        System.out.println("\nASCII values of lowercase letters (a-z):");
        System.out.println("Character: a, ASCII Value: " + (int)'a');
        System.out.println("Character: b, ASCII Value: " + (int)'b');
        // ... (continue for all lowercase letters)
        System.out.println("\nASCII values of some special characters:");
        System.out.println("Character: !, ASCII Value: " + (int)'!');
        System.out.println("Character: @, ASCII Value: " + (int)'@');
        System.out.println("Character: #, ASCII Value: " + (int)'#');
        System.out.println("Character: $, ASCII Value: " + (int)'$');
        System.out.println("Character: %, ASCII Value: " + (int)'%');
    }
}